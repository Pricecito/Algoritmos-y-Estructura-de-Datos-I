    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(indice<numeros.length)
        {
            numeros[indice]=Integer.parseInt(txtNum.getText());
            indice++;
            txtNum.setText(null);
            txtNum.requestFocus();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"VECTOR LLENO","ALERTA",JOptionPane.INFORMATION_MESSAGE);
            txtNum.setText(null);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        txtMostrar.setText(null);
        for(int i=0;i<numeros.length;i++)
        {
            txtMostrar.append(String.valueOf("POS ["+i+"]--> "+numeros[i])+"\n");
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        // TODO add your handling code here:
        int vbus,x=0;
        vbus=Integer.parseInt(txtBus.getText());
        for(int i=0;i<numeros.length;i++)
        {
            if(numeros[i]==vbus)
            {
                JOptionPane.showMessageDialog(null,"VALOR ENCONTRADO EN LA POSICIÃ“N: "+i,"ALERTA",JOptionPane.INFORMATION_MESSAGE);
                posicion=i;
                x=1;
            }
        }
        
        if(x==0)
            JOptionPane.showMessageDialog(null,"NO SE ENCONTRO NINGUN VALOR","IMPORTANTE",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_BtnBuscarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int vmod;
        vmod=Integer.parseInt(txtMod.getText());
        numeros[posicion]=vmod;
        JOptionPane.showMessageDialog(null,"VALOR MODIFICADO","IMPORTANTE",JOptionPane.INFORMATION_MESSAGE);
        txtBus.setText(null);
        txtMod.setText(null);
    }//GEN-LAST:event_jButton1ActionPerformed

