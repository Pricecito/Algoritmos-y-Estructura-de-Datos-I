    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(indice<nombres.length)
        {
            nombres[indice]=txtNom.getText();
            indice++;
            txtNom.setText(null);
            txtNom.requestFocus();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"VECTOR LLENO","ALERTA",
                    JOptionPane.INFORMATION_MESSAGE);
            txtNom.setText(null);
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void txtMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtMostrarActionPerformed
        // TODO add your handling code here:
        txtRes.setText(null);
        for(int i=0;i<indice;i++)
        {
            txtRes.append(String.valueOf(nombres[i])+"\n");
        }
    }//GEN-LAST:event_txtMostrarActionPerformed

