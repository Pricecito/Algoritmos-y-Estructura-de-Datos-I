    public void mostrarAleatorios() 
    {
        int cantidad;
        int i=1;

        cantidad = Integer.parseInt(txtCantidad.getText());
        txaResultado.setText("");
        
        while(i<=cantidad) 
        {
            generarAleatorio();
            txaResultado.append(numeroAleatorio+"\n");
            i++;
        }
    }
        
    public void generarAleatorio() 
    {
        numeroAleatorio = (int) (Math.random()*(100+1));
    }

