    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double ing,cc,c_inicial,resto,c_mensual;
        ing=Double.parseDouble(txtIngresos.getText());
        cc=Double.parseDouble(txtCostoCasa.getText());
        if(ing<2000)
        {
            c_inicial=cc*0.15;
            resto=cc-c_inicial;
            c_mensual=resto/120;
        }
        else
        {
            c_inicial=cc*0.30;
            resto=cc-c_inicial;
            c_mensual=resto/84;
        }
        //txaRes.setText(null);
        txaRes.append("CUOTA INICiAL: "+c_inicial+"\n");
        txaRes.append("CUOTA MENSUAL: "+c_mensual+"\n");
    }//GEN-LAST:event_btnCalcularActionPerformed

