    public void Agregar(int num)
    {
        if(indice<numeros.length)
        {
            numeros[indice]=num;
            indice++;

        }
        else
        {
            JOptionPane.showMessageDialog(null,"VECTOR LLENO","ALERTA",JOptionPane.INFORMATION_MESSAGE);
            //txtNum.setText(null);
        }
    }
    
    public void Mostrar()
    { 
        for(int i=0;i<indice;i++)
        {
            txtMostrar.append(String.valueOf("POS ["+i+"]--> "+numeros[i])+"\n");
        }
    }
    
    public int Buscar(int nbus)
    {
        int pos=-1;
        for(int i=0;i<indice;i++)
        {
            if(numeros[i]==nbus)
                pos=i;   
        }
        return pos;
    }
    
    public void EliminarNumero(int pos)
    {
        for(int i=pos;i<indice-1;i++)
        {
            numeros[i]=numeros[i+1];
        }
        
        int aux[]=new int[5];
        
        for(int i=0;i<indice-1;i++)
        {
            aux[i]=numeros[i];
        }
        numeros=aux;
        indice--;
    }

