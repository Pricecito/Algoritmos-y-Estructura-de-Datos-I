    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int nbus=Integer.parseInt(txtBus.getText());
        int pos=Buscar(nbus);
        if (pos!=-1)
            JOptionPane.showMessageDialog(null,"VALOR ENCONTRADO EN LA POSICIÓN: "+pos,"ALERTA",JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(null,"NO SE ENCONTRO NINGUN VALOR","IMPORTANTE",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        //int nbus=Integer.parseInt(txtBus.getText());
        int neli=Integer.parseInt(txtEli.getText());
        int pos=Buscar(neli);
        if (pos!=-1)
        {
            EliminarNumero(pos);
            JOptionPane.showMessageDialog(null,"VALOR ELIMINADO","IMPORTANTE",JOptionPane.INFORMATION_MESSAGE);
            txtBus.setText(null);
            txtEli.setText(null);
        }
        else
            JOptionPane.showMessageDialog(null,"NO SE PUEDE ELIMINAR, NO SE ENCONTRO NINGUN VALOR","IMPORTANTE",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnEliminarActionPerformed

