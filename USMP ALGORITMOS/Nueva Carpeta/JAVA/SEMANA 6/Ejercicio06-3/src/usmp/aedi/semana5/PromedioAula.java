    private void btnRegisrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisrarActionPerformed
        // TODO add your handling code here:

        String tex=txtReg.getText();
        if(tex.compareTo("")!=0)
        {
            if(indice<vPromedioAlumno.length)
            {
                double num=Double.parseDouble(txtReg.getText());
                char op=validarPromedioAlumno(num);
                if(op=='s')
                {
                    vPromedioAlumno[indice]=redondearDecimales(num);
                    indice++;
                }
                else
                    JOptionPane.showMessageDialog(null,"Promedio No Valido","ALERTA",JOptionPane.INFORMATION_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(null,"VECTOR LLENO","ALERTA",JOptionPane.INFORMATION_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null,"NO INGRESO NINGUN DATO","ALERTA",JOptionPane.WARNING_MESSAGE);
        
        txtReg.setText("");
        txtReg.requestFocus();       
    }//GEN-LAST:event_btnRegisrarActionPerformed

    private void btListarNotasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarNotasActionPerformed
        // TODO add your handling code here:
        txtRes.setText(null);
        if(indice!=0)
        {
            for(int i=0;i<indice;i++)
            {
                txtRes.append(String.valueOf(i+" ----------> "+vPromedioAlumno[i])+"\n");
            }
        }
        else
            JOptionPane.showMessageDialog(null,"No hay datos para listar","ALERTA",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btListarNotasActionPerformed

    private void CalcularPromedioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularPromedioActionPerformed
        // TODO add your handling code here:
        double prom;
        if(indice==vPromedioAlumno.length)
        {
            prom=redondearDecimales(calcularPromedio());
            JOptionPane.showMessageDialog(null,"PROMEDIO DEL AULA: "+prom,"IMPORTANTE",JOptionPane.INFORMATION_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null,"NO SE PUEDE CALCULAR EL PROMEDIO","ALERTA",JOptionPane.WARNING_MESSAGE);
            
    }//GEN-LAST:event_CalcularPromedioActionPerformed

