    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String mat=txtCurso.getText();
        if(mat.compareTo("")!=0){
            if(indice1<curso.length&&indice2<notas.length){
                Agregar(mat);
                Mostrar();
            }else{
                JOptionPane.showMessageDialog(this,"EXCEDE DE LOS 5 CURSOS");
            }
        }else{
            JOptionPane.showMessageDialog(this,"NO INGRESO DATOS","ALERTA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String teli=JOptionPane.showInputDialog(this,"INGRESE CURSO","Entrada",JOptionPane.QUESTION_MESSAGE);
        if(teli.compareTo("")!=0){
            teli=teli.toUpperCase();
            int pos=Buscar(teli);
            if(pos>=0){
                Eliminar(pos);
                Mostrar();
            }else{
                JOptionPane.showMessageDialog(this,"CURSO NO ENCONTRADO","ALERTA", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this,"CASILLA VACIA","ALERTA", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

