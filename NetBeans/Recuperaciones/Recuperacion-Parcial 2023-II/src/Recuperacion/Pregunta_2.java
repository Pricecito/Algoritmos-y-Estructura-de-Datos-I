    public void Agregar(String mat){
        x=true;
        curso[indice1]=mat.toUpperCase();
        indice1++;
        notas[indice2]=GenerarNota();
        indice2++;
    }
    public int GenerarNota(){
        int not=(int)(Math.random()*(20-0+1)+0);
        return not;
    }
    public void Mostrar(){
        txtA.setText(null);
        for(int i=0;i<indice1&&i<indice2;i++){
            txtA.append(curso[i]+"----"+notas[i]+"\n");
        }
    }
    public int Buscar(String teli){
        int pos=-1;
        for(int i=0;i<indice1&&i<indice2;i++){
            if(teli.compareTo(curso[i])==0){
                  pos=i;     
            }
        }
        return pos;
    }
    public void Eliminar(int pos){
        for(int j=pos;j<indice1-1;j++){
            curso[j]=curso[j+1];
            notas[j]=notas[j+1];
        }
        indice1--;
        indice2--;
        String auxA[]=new String[curso.length];
        int auxB[]=new int[notas.length];
        for(int c=0;c<indice1;c++){
            auxA[c]=curso[c];
            auxB[c]=notas[c];
        }
        curso=auxA;
        notas=auxB;
        
        JOptionPane.showMessageDialog(this, "El curso fue eliminado");
    }
    public void Promediar(){
        int suma=0;
        double prom;
        for(int i=0;i<indice1;i++){
            suma+=notas[i];
        }
        prom=suma*1.0/indice1;
        prom=Math.round(prom*100.0)/100.0;
        JOptionPane.showMessageDialog(this, "EL PROMEDIO ES: "+prom);
    }
