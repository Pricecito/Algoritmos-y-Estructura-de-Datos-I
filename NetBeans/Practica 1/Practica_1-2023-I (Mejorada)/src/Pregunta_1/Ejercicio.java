    public void registrar(int e){
        A[indiceA]=e;
        indiceA++;
    }
    public void validar(int e){
        if(e%2==0){
            cPar++;
        }else{
            cImP++;
        }
    }
    public void buscar(int e){
        int iPos=0;
        String texto="";
        boolean y=false;
        for(int c=0;c<indiceA;c++){
            if(A[c]==e){
                texto+=c+"; ";
                iPos++;
                y=true;
            }
        }
        if(y){
            JOptionPane.showMessageDialog(null,"Esta en la posicion "+texto+" y la cantidad de veces que se repite es "+iPos);
        }else{
            JOptionPane.showMessageDialog(null,"NUMERO NO ENCONTRADO","ERROR",JOptionPane.WARNING_MESSAGE);
        }
    }
    public void eliminar(int e){
        boolean x=false;
        for(int c=0;c<indiceA;c++){
            if(A[c]==e){
               for(int pos=c;pos<indiceA-1;pos++){
                   A[pos]=A[pos+1];
               }
               indiceA--;
               c=-1;
               x=true;
            }
        }
        if(x){
            int aux[]=new int[A.length];
            for(int i=0;i<indiceA;i++){
                aux[i]=A[i];
            }
            A=aux;
        }else{
             JOptionPane.showMessageDialog(null,"NUMERO NO ENCONTRADO","ERROR",JOptionPane.WARNING_MESSAGE);
        }
       
    }
    public void ordenar(){
        int x;
        for(int c=0;c<indiceA;c++){
            if(A[c]%2!=0){
                B[indiceB]=A[c];
                indiceB++;
            }  
        }
        x=indiceB;
        for(int c=0;c<indiceB;c++){
            for(int i=c+1;i<indiceB;i++){
                if(B[c]>=B[i]){
                    int aux=B[c];
                    B[c]=B[i];
                    B[i]=aux;
                }
            }
        }
        for(int c=indiceB;c<indiceA;c++){
            if(A[c]%2==0){
                B[indiceB]=A[c];
                indiceB++;
            }  
        }
        for(int c=x;c<indiceB;c++){
            for(int i=c+1;i<indiceB;i++){
                if(B[c]>=B[i]){
                    int aux=B[c];
                    B[c]=B[i];
                    B[i]=aux;
                }
            }
        }
        
    }
    public void mostrar(){
        String t=JOptionPane.showInputDialog("1.Vector A\n2.Vector B\n3. Ambos");
        int n=Integer.parseInt(t);
        txtA.setText(null);
        switch(n){
            default:
                
                JOptionPane.showMessageDialog(null,"ERROR","ERROR",JOptionPane.ERROR_MESSAGE);
                break;
            case 1: 
                for(int c=0;c<indiceA;c++){
                    if(c<indiceA-1){
                        txtA.append(A[c]+"; ");
                    }else{
                        txtA.append(String.valueOf(A[c]));
                    }
                }
                break;
            case 2:
                for(int c=0;c<indiceB;c++){
                    if(c<indiceB-1){
                        txtA.append(B[c]+"; ");
                    }else{
                        txtA.append(String.valueOf(B[c]));
                    }
                }
                break;
            case 3:
                txtA.append("VECTOR A: \n");
                for(int c=0;c<indiceA;c++){
                    if(c<indiceA-1){
                        txtA.append(A[c]+"; ");
                    }else{
                        txtA.append(String.valueOf(A[c]));
                    }
                }
                txtA.append("\n");
                txtA.append("VECTOR B \n");
                for(int c=0;c<indiceB;c++){
                    if(c<indiceB-1){
                        txtA.append(B[c]+"; ");
                    }else{
                        txtA.append(String.valueOf(B[c]));
                    }
                }           
        }
    }
