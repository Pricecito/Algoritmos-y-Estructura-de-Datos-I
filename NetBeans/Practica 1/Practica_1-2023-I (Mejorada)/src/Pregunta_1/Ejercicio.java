    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(indiceA<A.length){
            String e=JOptionPane.showInputDialog("Digite edad:");
        int edad=Integer.parseInt(e);
        
        if(edad>=15){
            registrar(edad);
            validar(edad);
            JOptionPane.showMessageDialog(null, "EDAD REGISTRADA","EXITO",JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null,"EDAD NO ACEPTADA","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }else{
            JOptionPane.showMessageDialog(this,"Vector Lleno","ERROR",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

