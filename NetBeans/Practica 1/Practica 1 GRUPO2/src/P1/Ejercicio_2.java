    private void btnRegGrupsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegGrupsActionPerformed
        grupos=new int[Integer.parseInt(JOptionPane.showInputDialog("Digite cantidad de grupos:"))];
        for(int c=0;c<grupos.length;c++){
            grupos[c]=Integer.parseInt(JOptionPane.showInputDialog("Introduzca nombre del grupo "+(c+1)+":"));
        }
    }//GEN-LAST:event_btnRegGrupsActionPerformed

    private void btnAlumnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlumnosActionPerformed
        alumnos=new int[Integer.parseInt(JOptionPane.showInputDialog("Digite cantidad de alumnos de grupo:"))];
        for(int c=0;c<grupos.length;c++){
            for(int i=0;i<alumnos.length;i++){
                alumnos[i]=Integer.parseInt(JOptionPane.showInputDialog("Introduzca nombre de alumno "+(i+1)+" del grupo "+(c+1)+":"));
            }
        }
        
        
    }//GEN-LAST:event_btnAlumnosActionPerformed

    private void btnMateriasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMateriasActionPerformed
        materias=new int[Integer.parseInt(JOptionPane.showInputDialog("Digite cantidad de materias:"))];
        proms=new double [materias.length];
        for(int c=0;c<grupos.length;c++){
            for(int i=0;i<alumnos.length;i++){
                for(int j=0;j<alumnos.length;j++){
                    materias[j]=Integer.parseInt(JOptionPane.showInputDialog("Introduzca nombre de materia del alumno "+(i+1)+" del grupo "+(c+1)+", materia "+(j+1)+":"));
                    

                }
            }
        }
    }//GEN-LAST:event_btnMateriasActionPerformed

