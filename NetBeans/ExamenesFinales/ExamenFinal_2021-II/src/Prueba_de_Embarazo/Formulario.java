    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(indice<pac.length){
            String t=txtNombre.getText().trim().toUpperCase();
            if(t.equals("")==false){
                String n=t;
                int c;
                do{
                    c=Utilitario.generarAleatorio(3500, 4499);
                }while(codigoRepetido(c)==true);
                int edad=Utilitario.generarAleatorio(1, 45);
                String sexo=Utilitario.generarAleatorio();
                if(edad>=15){
                    if(sexo.equals("FEMENINO")){
                        pac[indice]=new Paciente(c, n, edad, sexo);
                        indice++;
                    }else{
                        Utilitario.Mensaje("EL SEXO ES MASCULINO, NO SE REGISTRO");
                    }
                }else{
                    Utilitario.Mensaje("MENOR DE EDAD, VISITE A SU MEDICO");
                }
            }else{
                Utilitario.MensajeError("INSERTAR NOMBRE");
            }
        }else{
            Utilitario.MensajeError("NO SE PUEDE REGISTRAR MAS PACIENTES");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        if(indice>0){
            txtA.setText(null);
            for(int c=0;c<indice;c++){
                txtA.append("\nPACIENTE NRO: "+(c+1)+"\n");
                txtA.append("   CODIGO: "+pac[c].getCod()+"\n");
                txtA.append("   NOMBRE: "+pac[c].getNombre()+"\n");
                txtA.append("   EDAD: "+pac[c].getEdad()+"\n");
                txtA.append("   SEXO: "+pac[c].getSexo()+"\n");
            }
        }else{
            Utilitario.MensajeError("NO HAY PACIENTE PARA LISTAR");
        }
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnPruebaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPruebaActionPerformed
        int c=Integer.parseInt(Utilitario.ingreso("INGRESAR CODIGO DE PACIENTE"));
        int pos=Buscar(c);
        if(pos>=0){
            if(pac[pos].getIndice_p()<pac[pos].getPrueba().length){
                int d=Integer.parseInt(Utilitario.ingreso("INGRESAR TIPO DE PRUEBA\n1.PRUEBA DE SANGRE\n2.PRUEBA DE ORINA"));
                switch(d){
                    case 1: pac[pos].setPrueba(new P_Sangre().realizarPrueba()); 
                    Utilitario.Mensaje("SE REALIZO LA PRUEBA DE SANGRE");
                    break;
                    case 2: pac[pos].setPrueba(new P_Orina().realizarPrueba());
                    Utilitario.Mensaje("SE REALIZO LA PRUEBA DE ORINA");
                    break;
                    default:Utilitario.MensajeError("LA RESPUESTA NO CORRESPONDE CON LAS ALTERNATIVAS");
                }
            }else{
                Utilitario.MensajeError("NO SE PUEDE REALIZAR MAS PRUEBAS");
            }
        }else{
            Utilitario.MensajeError("NO SE ENCONTRO AL PACIENTE");
        }
    }//GEN-LAST:event_btnPruebaActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int cod=Integer.parseInt(Utilitario.ingreso("INGRESAR CODIGO DE PACIENTE"));
        int pos=Buscar(cod);
        if(pos>=0){
            txtA.setText("BUSQUEDA DEL PACIENTE\n");
            txtA.append("--------------------------------\n");
            txtA.append("CODIGO: "+pac[pos].getCod()+"\n");
            txtA.append("NOMBRE: "+pac[pos].getNombre()+"\n");
            txtA.append("EDAD: "+pac[pos].getEdad()+"\n");
            txtA.append("SEXO: "+pac[pos].getSexo()+"\n");
            txtA.append("PRUEBAS \n");
            for(int c=0;c<pac[pos].getIndice_p();c++){
                txtA.append(" -- "+pac[pos].getPrueba()[c]+"\n");
            }
        }else{
            Utilitario.Mensaje("NO SE ENCONTRO AL PACIENTE");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

