    public boolean codigoRepetido(int cod){
        for(int c=0;c<indice;c++){
            if(pac[c].getCod()==cod){
                return true;
            }
        }
        return false;
    }
    public int Buscar(int cod){
        for(int i=0;i<indice;i++){
            if(pac[i].getCod()==cod){
                return i;
            }
        }
        return -1;
    }
    
