    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        int cod=0;
        boolean flag=true;
        
        if (indice<pac.length){
            do{
                cod=Utilitario.generarAleatorio(1000, 1999);
                flag=codigoRepetido(cod);
            }while(flag==true);
            String nom=txtNombre.getText();
            int edad=Utilitario.generarAleatorio(1, 30);
            String sexo=Utilitario.generarAleatorio();
            if(edad>=12)
            {
                if(sexo.equals("F"))
                {
                    pac[indice]=new Paciente(cod,nom,edad,sexo);
                    indice++;
                }
                else
                    Utilitario.mensaje("EL SEXO ES MASCULINO, NO SE REGISTRO");
            }
            else
                Utilitario.mensaje("MENOR DE EDAD, VISITE A SU MÉDICO");}
        else
            Utilitario.mensaje("NO SE PUEDE REGISTRAR MÁS PACIENTES");
        txtNombre.setText(null);
        txtNombre.requestFocus();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int pos=buscar(Integer.parseInt(Utilitario.ingreso("INGRESE CÓDIGO A BUSCAR")));
        if (pos!=-1)
        {
            txaRes.setText(null);
            txaRes.append("BUSQUEDA DEL PACIENTE\n");
            txaRes.append("-------------------------------------------\n");
            txaRes.append("CODIGO: "+pac[pos].getCod()+"\n");
            txaRes.append("NOMBRE: "+pac[pos].getNombre()+"\n");
            txaRes.append("EDAD: "+pac[pos].getEdad()+"\n");
            txaRes.append("SEXO: "+pac[pos].getSexo()+"\n");
            txaRes.append("PRUEBAS\n");
            for(int i=0;i<pac[pos].getIndice_p();i++)
            {
                txaRes.append("  --  "+pac[pos].getPrueba()[i]+"\n");
            } 
        }
        else
            Utilitario.mensaje("NO SE ENCONTRO AL PACIENTE");
    }//GEN-LAST:event_btnBuscarActionPerformed

