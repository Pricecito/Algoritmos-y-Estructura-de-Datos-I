    private void btnPruebaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPruebaActionPerformed
        // TODO add your handling code here:
        int pos=buscar(Integer.parseInt(Utilitario.ingreso("INGRESE CÓDIGO A BUSCAR")));
        if (pos!=-1)
        {
            
            if(pac[pos].getIndice_p()<pac[pos].getPrueba().length)
            {
                int op=Integer.parseInt(Utilitario.ingreso("INGRESE TIPO DE PRUEBA"+
                "\n1. PRUEBA DE SANGRE\n2. PRUEBA DE ORINA"));
                switch (op){
                        case 1:
                            pac[pos].setPrueba(new P_Sangre().realizarPrueba());
                            Utilitario.mensaje("SE REALIZÓ LA PRUEBA DE SANGRE");
                            break;
                        case 2:
                            pac[pos].setPrueba(new P_Orina().realizarPrueba());
                            Utilitario.mensaje("SE REALIZÓ LA PRUEBA DE ORINA");
                            break;
                        default:
                            Utilitario.mensaje("ERROR!!! .. INGRESE LA OPCIÓN CORRECTA");}
            }
            else
                Utilitario.mensaje("NO SE PERMITEN MAS PRUEBAS");
        }
        else
            Utilitario.mensaje("NO SE ENCONTRO AL PACIENTE");
    }//GEN-LAST:event_btnPruebaActionPerformed

