    public boolean codigoRepetido(int cod)
    {
        boolean flag=false;
        for(int i=0;i<indice;i++)
        {
            if (pac[i].getCod()==cod)
                flag=true;
            else
                flag=false;
        }
        return flag;
    }
    
    public int buscar(int cod)
    {
        int pos=-1;
        for(int i=0;i<indice;i++)
        {
            if(pac[i].getCod()==cod)
            {
                pos=i;
                break;
            }             
        }
        return pos;
    }

