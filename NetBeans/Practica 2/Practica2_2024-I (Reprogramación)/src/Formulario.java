    private void btnCamionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCamionActionPerformed
        String t=JOptionPane.showInputDialog("INGRESE TIPO").toUpperCase();
        switch(t){
            case "SEMILIVIANO":
            case "MEDIANO": 
                double carga=Double.parseDouble(JOptionPane.showInputDialog("INGRESE CARGA"));
                Camion c=new Camion("ABC-123","JAC","D300",t,carga);
                txtA.setText(null);
                mostrarCamion(c);
                ve[1]=c;
                c.mostrar();
                break;
            default:JOptionPane.showMessageDialog(null,"TIPO INCORRECTO, VUELVA A INGRESARLO");
        }
        
    }//GEN-LAST:event_btnCamionActionPerformed

