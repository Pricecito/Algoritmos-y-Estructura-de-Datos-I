    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(indice<vector.length){
            String nombre=txtNombre.getText().toUpperCase();
            String e=txtEdad.getText();
            if(nombre.equals("")!=true&&e.equals("")!=true){
                int Tipo=Integer.parseInt(JOptionPane.showInputDialog(null,"1. EMPLEADO\n2.ESTUDIANTE","ENTRADA",JOptionPane.QUESTION_MESSAGE));
                int edad=Integer.parseInt(e);
                switch(Tipo){
                    case 1:double salario=Double.parseDouble(JOptionPane.showInputDialog(null,"INSERTAR SALARIO","ENTRADA",JOptionPane.QUESTION_MESSAGE));
                            vector[indice]=new Empleado("EMPLEADO",nombre, edad, salario);
                            indice++;
                            break;
                    case 2:String carrera=JOptionPane.showInputDialog(null,"CARRERA","INFORMACION",JOptionPane.QUESTION_MESSAGE).toUpperCase();
                            vector[indice]=new Estudiante("ESTUDIANTE",nombre, edad, carrera);
                            indice++;
                            break;
                    default: JOptionPane.showMessageDialog(null,"RESPUESTA INCORRECTA","INFORMACION",JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null,"ESPACIOS VACIOS","INFORMACION",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"NO SE PUEDEN ALMACENAR MAS PERSONAS","INFORMACION",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        int Tipo=Integer.parseInt(JOptionPane.showInputDialog(null,"1. EMPLEADO\n2. ESTUDIANTE","ENTRADA",JOptionPane.QUESTION_MESSAGE));
        switch(Tipo){
            case 1:MostrarEmpleado();
                    break;
            case 2:MostrarEstudiante();
                    break;
            default: JOptionPane.showMessageDialog(null,"RESPUESTA INCORRECTA","INFORMACION",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int Tipo=Integer.parseInt(JOptionPane.showInputDialog(null,"1. EMPLEADO\n2.ESTUDIANTE","ENTRADA",JOptionPane.QUESTION_MESSAGE));
        switch(Tipo){
            case 1:
                String nom1=JOptionPane.showInputDialog(null,"INSERTAR NOMBRE A BUSCAR","INFORMACION",JOptionPane.QUESTION_MESSAGE).toUpperCase();
                int pos1=Buscar(nom1);
                if(pos1!=-1){
                    vector[pos1].modificarDatos();
                    MostrarEmpleado();
                }else{
                    JOptionPane.showMessageDialog(null,"EMPLEADO NO ENCONTRADO","INFORMACION",JOptionPane.WARNING_MESSAGE);
                }
                break;
            case 2:
                String nombre=JOptionPane.showInputDialog(null,"INSERTAR NOMBRE A BUSCAR","INFORMACION",JOptionPane.QUESTION_MESSAGE).toUpperCase();
                int pos2=Buscar(nombre);
                if(pos2!=-1){
                    vector[pos2].modificarDatos();
                    MostrarEstudiante();
                }else{
                    JOptionPane.showMessageDialog(null,"ESTUDIANTE NO ENCONTRADO","INFORMACION",JOptionPane.WARNING_MESSAGE);
                }
                break;
            default: JOptionPane.showMessageDialog(null,"RESPUESTA INCORRECTA","INFORMACION",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

