    public int Buscar(String bus){
        int pos=-1;
        for(int c=0;c<indice;c++){
            if(vector[c].getNombre().equals(bus)){
                pos=c;
            }
        }
        return pos;
    }
    public void MostrarEmpleado(){
        txtA.setText("EMPLEADOS\n");
        for(int c=0;c<indice;c++){
            if(vector[c].tipo.equals("EMPLEADO")){
                Empleado Em=(Empleado)(vector[c]);
                txtA.append("-----------------------------------\n");
                txtA.append("NOMBRE: "+Em.getNombre()+"\n");
                txtA.append("EDAD: "+Em.getEdad()+"\n");
                txtA.append("SALARIO: "+Em.getSalario()+"\n");
            }
        } 
    }
    public void MostrarEstudiante(){
        txtA.setText("ESTUDIANTES\n");
        for(int c=0;c<indice;c++){
            if(vector[c].tipo.equals("ESTUDIANTE")){
                Estudiante Es=(Estudiante)(vector[c]);
                txtA.append("-----------------------------------\n");
                txtA.append("NOMBRE: "+Es.getNombre()+"\n");
                txtA.append("EDAD: "+Es.getEdad()+"\n");
                txtA.append("CARRERA: "+Es.getCarrera()+"\n");
            }
        } 
    }
