    public void edad(){
        if(edad<=1){
                qC1++;
        }else if(edad<3){
                qC2++;
        }else{
                qC3++;
        }
    }
    public void animal(int ani){
        switch(ani){
            case 0:qTot=20;
            break;
            case 1:qTot=15;
            break;
            default:qTot=40;
        } 
    }
    public void promedioCat1(){
        pC1=qC1*100.0/qTot;
    }
    public void promedioCat2(){
        pC2=qC2*100.0/qTot;
    }
    public void promedioCat3(){
        pC3=qC3*100.0/qTot;
    }
