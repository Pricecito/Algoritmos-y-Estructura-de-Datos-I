    public double calcularHorasExtras(int qH,double mH){
        double qHoE1,qHoE2,mHoE;
        if(qH<=40){
            qHoE1=0;
            mHoE=qH*qHoE1;
        }else if(qH<=48){
            qHoE1=qH-40;
            mHoE=mH*2*qHoE1;
        }else{
            qHoE2=qH-48;
            qHoE1=qH-40-qHoE2;
            mHoE=(mH*2*qHoE1)+(mH*3*qHoE2);
        }
        return mHoE;
    }
