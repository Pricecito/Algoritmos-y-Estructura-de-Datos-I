    public void calcularEnganche(){
        double pEn, pMen;
        qIn=Integer.parseInt(txtqIn.getText());
        mCaTot=Integer.parseInt(txtPviv.getText());
        if(qIn<2000){
            pEn=mCaTot*0.15;
            pMen=(mCaTot-pEn)/120.0;
        }else{
            pEn=mCaTot*0.30;
            pMen=(mCaTot-pEn)/84.0;
        }
        redondearMensualidad(pMen);
        redondearPrecio(pEn); 
    }
    public void redondearMensualidad(double pMen){
        pMen=Math.rint(pMen*100)/100;
        txtA.setText("Y el monto a pagar por cuota sera de "+pMen+"\n");
    }
    public void redondearPrecio(double pEn){
        pEn=Math.rint(pEn*100)/100;
        txtA.append("El monto a pagar por enganche serÃ¡ de: "+pEn);
    }
