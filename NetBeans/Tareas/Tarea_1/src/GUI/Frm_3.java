    public void calcularGeneroYedad() {
        switch(gen){
            case 0: qM++;
                    edaM+=edad;
                    break;
            default:qF++;
                    edaF+=edad;
        }
    }
    public double calcularPromedioMas(){
        double prom;
        prom=edaM*1.0/qM;
        return prom;
    }
    public double calcularPromedioFem(){
        double prom;
        prom=edaF*1.0/qF;
        return prom;
    }
    public void edadMenor(){
        if(eMe>edad){
            eMe=edad;
        }
    }
