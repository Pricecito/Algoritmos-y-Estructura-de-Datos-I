    public boolean verificarClienteRegistrado(String n){
        for(int c=indice-1;c>=0;c--){
            if(vDni[c].equals(n))
                return true;
        }
        return false;
    } 
    public void Registrar(){
        String D=txtDNI.getText().trim();
        if(verificarClienteRegistrado(D)==false){
            vApellido[indice]=txtApellido.getText().trim();
            vEdad[indice]=Integer.parseInt(txtEdad.getText().trim());
            vDni[indice]=D;
            indice++;
        }else{
            UtilitarioCovid.setMensaje("Solo se realizar una prueba por persona");
        }
    }
    public int Buscar(String n){
        int pos=-1;
        for(int c=0;c<indice;c++){
            if(n.equals(vDni[c])){
                pos=c;
            }
        }
        return pos;
    }
    public void Mostrar(){
        txtA.setText("LISTA DE RESULTADOS\n");
        for(int i=0;i<indice;i++){
            txtA.append("PACIENTE "+(i+1)+"\n");
            txtA.append("   APELLIDO: "+vApellido[i]+"\n");
            txtA.append("   EDAD: "+vEdad[i]+"\n");
            txtA.append("   DNI: "+vDni[i]+"\n");
            if(vTipoPrueba[i]=='M'){
                txtA.append("   TIPO DE PRUEBA: MOLECULAR\n");
            }else if(vTipoPrueba[i]=='R'){
                txtA.append("   TIPO DE PRUEBA: RAPIDA\n");
            }else{
                txtA.append("   TIPO DE PRUEBA: NO REALIZADA\n");
            }
            if(vResultado[i]=='P'){
                txtA.append("   RESULTADO: POSITIVO\n");
            }else if(vResultado[i]=='N'){
                txtA.append("   RESULTADO: NEGATIVA\n");
            }else{
                txtA.append("   RESULTADO: NO REALIZADA\n");
            }
            txtA.append("\n");
        }
        
    }
