    private void btnRegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarClienteActionPerformed
        String t1=txtApellido.getText(),t2=txtEdad.getText(),t3=txtDNI.getText();
        if(indice<vApellido.length){
            if(t1.trim().length()>2&&t2.compareTo("")!=0&&t3.trim().compareTo("")!=0){
                Registrar();
            }else{
                UtilitarioCovid.setMensajeError("Los espacios de textos tiene que tener como minimo 2 caracteres.");
            }
        }else{
            UtilitarioCovid.setMensajeError("SOLO SE PERMITEN 15 PACIENTES");
        }
    }//GEN-LAST:event_btnRegistrarClienteActionPerformed

    private void btnRealizarExamenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarExamenActionPerformed
        int p=Buscar(UtilitarioCovid.getMensaje("INSERTAR DNI DEL PACIENTE:"));
        if(p!=-1){
            int r=Integer.parseInt(UtilitarioCovid.getMensaje("Tipo de pruba:\n1 Prueba Molecular\n2 Prueba RÃ¡pida"));
            switch(r){
                case 1:vTipoPrueba[p]='M';
                        vResultado[p]=new PruebaMolecular().realizarPrueba();
                        break;
                case 2:vTipoPrueba[p]='R';
                        vResultado[p]=new PruebaRapida().realizarPrueba();
                        break;
                default:UtilitarioCovid.setMensajeError("LA RESPUESTA NO COINCIDE CON NINGUNA OPCION");
            }
            Mostrar();
        }else{
            UtilitarioCovid.setMensajeError("DEBE REGISTRARSE PRIMERO");
        }
    }//GEN-LAST:event_btnRealizarExamenActionPerformed

