    public boolean codigoRepetido(int cod){
        boolean x=false;
        for(int c=indice-1;c>=0;c--){
            if(cod==vCodigo[c])
                x=true;
        }
        return x;
    }
    public void Mostrar(){
        txtA.setText(null);
        for(int c=0;c<indice;c++){
            txtA.append("POS: ["+c+"]\n");
            if(Tipo[c]=='T'){
                txtA.append("Tipo : Camión" +"\n");
            }else{
                txtA.append("Tipo : Auto" +"\n");
            }
            txtA.append("CODIGO: "+vCodigo[c]+"\n");
            txtA.append("Resultado: "+vResultado[c]+"\n-\n");
        }
    }
