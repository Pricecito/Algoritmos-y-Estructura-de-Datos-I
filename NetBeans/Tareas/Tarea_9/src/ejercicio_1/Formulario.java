    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String t=(txtTipoCarro.getText()).trim();
        if(t.compareTo("")!=0&&t.length()==1){
            switch(t.charAt(0)){
                case 'A':xV=true;
                case 'T':int n;
                        do{
                            n=Utilitario.generarAleatorio(1000,5000);
                        }while(codigoRepetido(n)==true);
                        vCodigo[indice]=n;
                        Tipo[indice]=t.charAt(0);
                        indice++;
                        Mostrar();
                        break;
                default:JOptionPane.showMessageDialog(null,"EL CARACTER NO CONCUERDA CON LOS TIPOS DE VEHICULO","ERROR",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"EL TEXTO NO CORRESPONDE","ERROR",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

