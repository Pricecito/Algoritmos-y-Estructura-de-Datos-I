    public void sumarDigitos(int num){
        int log,suma=0,ent;
        char car;
        String numSt;
        numSt=String.valueOf(num);
        log=numSt.length();
        for(int c=0;c<log;c++){
            car=numSt.charAt(c);
            suma+=Character.getNumericValue(car);
        }
        txtSum.setText(String.valueOf(suma));
        
    }
    public void invertirNumero(int num){
        int log;
        char car;
        String numSt,reves="";
        numSt=String.valueOf(num);
        log=numSt.length();
        for(int c=log-1;c>=0;c--){
            car=numSt.charAt(c);
            reves+=car;
        }
        txtInv.setText(reves);
    }
