    public void simularVotacion(int tot){
        String msj;
        votoF=(int)(Math.random()*(tot-0+1)+0);
        votoC=(int)(Math.random()*((tot-votoF)-0+1)+0);
        votoA=tot-(votoC+votoF);
        txtVotFav.setText(String.valueOf(votoF));
        txtVotCon.setText(String.valueOf(votoC));
        txtVotAbs.setText(String.valueOf(votoA));
        msj=resultado();
        txtRes.setText(msj);
    }
    public String resultado(){
        String msj;
        if(votoF>votoC){
            msj="APROBADO";
        }else{
            msj="DESAPROBADO";
        }
        return msj;
    }
