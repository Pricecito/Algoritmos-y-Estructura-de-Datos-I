    public void contarVocales(){
        int log;
        char let;
        cant=0;
        nombre=txtNom.getText().toLowerCase();
        log=nombre.length();
        for(int x=0;x<log;x++){
            let=nombre.charAt(x);
            switch(let){
                case'a':
                case'e':
                case'i':
                case'o':
                case'u':cant++;break;
            }
        }
    }
