    private void btnManzanaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManzanaActionPerformed
        Manzana E=new Manzana();
        int color=Integer.parseInt(JOptionPane.showInputDialog(null,"1.VERDE \n 2.ROJIZA \n 3.AMARILLENTA","COLOR INGRESE NUMERO DE ALTERNATIVA",JOptionPane.QUESTION_MESSAGE));
        if(color>0&&color<=3){
            int consistencia=Integer.parseInt(JOptionPane.showInputDialog(null,"1.ARINOSA \n 2.CRUJIENTE","PULPA INGRESE NUMERO DE ALTERNATIVA",JOptionPane.QUESTION_MESSAGE));
            if(consistencia>0&&consistencia<=2){
                int sabor=Integer.parseInt(JOptionPane.showInputDialog(null,"1.AMARGO \n 2.DULCE","SABOR INGRESE NUMERO DE ALTERNATIVA",JOptionPane.QUESTION_MESSAGE));
                if(sabor>0&&sabor<=2){
                    E.Modificar(color, consistencia, sabor);
                    JOptionPane.showMessageDialog(null,"COLOR: "+E.getColor()+"\n CONSISTENCIA: "+E.getConsistencia()+"\n SABOR: "+E.getSabor(),"MANZANA",JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null,"NUMERO NO VALIDO","ALERTA",JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null,"NUMERO NO VALIDO","ALERTA",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"NUMERO NO VALIDO","ALERTA",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnManzanaActionPerformed

