    public void Agregar(){
        if(indice==4){
            JOptionPane.showMessageDialog(this,"NO SE PUEDEN AÑADIR MAS NUMEROS");
        }else{
            vec[indice]=numeros;
            indice++;
        } 
    }
    public void Mostrar(){
        txtA.setText(null);
        for(int i=0;i<indice;i++){
            txtA.append(vec[i]+"\n");            
        }
    }
    public void Buscar(){
        x=false;
        for(int c=0;c<vec.length;c++){
            if(nBus==vec[c]){
                x=true;
                JOptionPane.showMessageDialog(this,"Se encontro el numero en la posicion "+c);
            }
        }
    }
    public void Modificar(int nMod){
        txtA.setText(null);
        for(int i=0;i<vec.length;i++){
            if(nBus==vec[i]){
                vec[i]=nMod;                
            }
        }
        JOptionPane.showMessageDialog(this,"El numero fue modificado");
    }
