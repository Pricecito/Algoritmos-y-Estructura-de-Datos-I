    public void Agregar(){
        if(indice==4){
            JOptionPane.showMessageDialog(this,"Solo se pueden insertar 4 n√∫meros");
        }else{
            vec[indice]=numeros;
            indice++;
        } 
    }
    public void Mostrar(){
        txtA.setText("");
        for(int c=0;c<indice;c++){
            txtA.append(vec[c]+"\n");
        }
    }
    public int Buscar(int nBus){
        int control=0;
        for(int i=0;i<vec.length;i++){
            if(vec[i]==nBus){
                vec[i]=nBus;
                control=1;
                JOptionPane.showMessageDialog(this,"Se encontro el numero en la posicion "+i);
            }
        }
        return control;
    }
    public void EliminarNumero(int veli){
        boolean y=false;
        int pos;
        for(int i=0;i<indice;i++){
            if(veli==vec[i]){
                pos=i;
                y=true;
                for(int j=pos;j<indice-1;j++){
                    vec[j]=vec[j+1];
                }        
            }
        }
        if(y==true){
            int aux[]=new int[4];
                for(int c=0;c<indice-1;c++){
                    aux[c]=vec[c];
                }
            vec=aux;
            indice--;
            JOptionPane.showMessageDialog(this, "El numero fue eliminado");
        }else{
           JOptionPane.showMessageDialog(this, "El numero buscado no existe");
        }
    }
