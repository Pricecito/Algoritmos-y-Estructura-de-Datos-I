    public void Poblar(){
        for(int c=0;c<vecA.length;c++){
            vecA[c]=(int)(Math.random()*(50-0+1)+0);
        }
        for(int c=0;c<vecB.length;c++){
            vecB[c]=(int)(Math.random()*(50-0+1)+0);
        }
    }
    public void Multiplicar(){
        indice=0; 
            for(int i=0;i<20;i++){
            if(i==0){
                vecC[indice]=vecA[i]*vecB[vecB.length-1];
            }else{
                vecC[indice]=vecA[i]*vecB[vecB.length-(i+1)];
            }
                indice++;
            }
        
        
        
    }
