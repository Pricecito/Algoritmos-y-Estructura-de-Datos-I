    public void Ingresar(){
        if(indice<vec.length){
            vec[indice]=numeros;
            indice++;
        }else{
            JOptionPane.showMessageDialog(this,"El Vector esta lleno !!!!!!!!");
        }
    }
    public int qCeros(){
        int cCeros=0;
        for(int c=0;c<indice;c++){
            if(vec[c]==0){
                cCeros++;
            }
        }
        return cCeros;
    }
    public int qNegativos(){
        int cNeg=0;
        for(int c=0;c<indice;c++){
            if(vec[c]<0){
                cNeg++;
            }
        }
        return cNeg;
    }
    public int qPositivos(){
        int cPos=0;
        for(int c=0;c<indice;c++){
            if(vec[c]>0){
                cPos++;
            }
        }
        return cPos;
    }
    public int SumaNegativos(){
        int sumaNeg=0;
        for(int c=0;c<indice;c++){
            if(vec[c]<0){
                sumaNeg-=vec[c];
            }
        }
        return sumaNeg;
    }
    public int SumaPositivos(){
        int sumaPos=0;
        for(int c=0;c<indice;c++){
            if(vec[c]>0){
                sumaPos+=vec[c];
            }
        }
        return sumaPos;
    }
    
