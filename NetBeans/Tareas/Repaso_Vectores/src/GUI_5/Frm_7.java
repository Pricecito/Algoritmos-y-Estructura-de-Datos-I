    public void Poblar(){
        for(int c=0;c<FIB.length;c++){
            if(c<2){
                FIB[c]=c;
            }else{
                FIB[c]=FIB[c-1]+FIB[c-2];
            }
        }
    }
    public void Mostrar(){
        for(int i=0;i<FIB.length;i++){
            if(i<FIB.length-1){
                txtA.append(FIB[i]+"; ");
            }else{
                txtA.append(FIB[i]+"");
            }
            
        }
    }
