    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String tex1=txtLadoA.getText();
        String tex2=txtLadoB.getText();
        String tex3=txtLadoC.getText();
        String tex4=txtLadoD.getText();
        if(tex1.compareTo("")!=0&&tex2.compareTo("")!=0&&tex3.compareTo("")!=0&&tex4.compareTo("")!=0){
            int cuadrilatero=cboCuadrilatero.getSelectedIndex();
            double ladoA=Double.parseDouble(tex1);
            double ladoB=Double.parseDouble(tex2);
            double ladoC=Double.parseDouble(tex3);
            double ladoD=Double.parseDouble(tex4);
            double alt;
            switch(cuadrilatero){
                case 0: Cuadrado B=new Cuadrado(ladoA, ladoB, ladoC, ladoD);
                        if(B.esCuadrado()){
                            Mostrar(B);
                        }
                        else{
                            JOptionPane.showMessageDialog(null,"ESTE NO ES UN CUADRADO","ALERTA",JOptionPane.WARNING_MESSAGE);
                        }
                        break;
                case 1: Rectangulo C=new Rectangulo(ladoA, ladoB, ladoC, ladoD);
                        if(C.esRectangulo()){
                            Mostrar(C);
                        }
                        else{
                            JOptionPane.showMessageDialog(null,"ESTE NO ES UN RECTANGULO","ALERTA",JOptionPane.WARNING_MESSAGE);
                        }
                        break;
                case 2: Paralelogramo D=new Paralelogramo(ladoA, ladoB, ladoC, ladoD);
                        if(D.esParalelogramo()){
                            Mostrar(D);
                        }
                        else{
                            JOptionPane.showMessageDialog(null,"ESTE NO ES UN PARALELOGRAMO","ALERTA",JOptionPane.WARNING_MESSAGE);
                        }
                        break;
                default:Trapezoide E=new Trapezoide(ladoA, ladoB, ladoC, ladoD);
                        if(E.esTrapezoide()){
                            Mostrar(E);
                        }
                        else{
                            JOptionPane.showMessageDialog(null,"ESTE NO ES UN TRAPEZOIDE","ALERTA",JOptionPane.WARNING_MESSAGE);
                        }
            }
        }else{
            JOptionPane.showMessageDialog(null,"LAS MEDIDAS NO CORRESPONDEN A LA DE UN CUADRILATERO","ALERTA",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

