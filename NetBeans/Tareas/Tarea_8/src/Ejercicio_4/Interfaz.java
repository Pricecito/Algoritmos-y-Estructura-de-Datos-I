    private void btnDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatosActionPerformed
        int doc=cboDoc.getSelectedIndex();
        switch(doc){
            case 0: String fPago=JOptionPane.showInputDialog(null,"1.Al credito\n2.Al Contado");
                    if((fPago.toUpperCase()).compareTo("AL CREDITO")!=0&&(fPago.toUpperCase()).compareTo("AL CONTADO")!=0){
                        JOptionPane.showMessageDialog(null,"Esta no es un forma de pago aceptada","ERROR",JOptionPane.WARNING_MESSAGE);
                    }else{
                        vecFac[indFac]=new Factura(Fecha(),fPago);
                        indFac++;
                    }
                    break;
            case 1: String t=JOptionPane.showInputDialog(null,"INGRESAR TELEFONO");
                    if(t.length()!=9){
                            JOptionPane.showMessageDialog(null,"El numero debe tener 9 digitos","ERROR",JOptionPane.WARNING_MESSAGE);
                    }else{
                        int tPedido=JOptionPane.showConfirmDialog(null, "Es un pedido urgente?");
                        if(tPedido==0){
                            vecPed[indPed]=new PedidoUrgente(Fecha(),Fecha(),t);
                            indPed++;
                        }else{
                            vecPed[indPed]=new Pedido(Fecha(),Fecha(),t);
                            indPed++;
                        }
                    }
                    break;
            default:vecCV[indCon]=new CV(Fecha());
                    indCon++;
        }
    }//GEN-LAST:event_btnDatosActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        String tp=JOptionPane.showInputDialog(null,"1. FACTURAS\n2. PEDIDOS\n3. CONTRATOS\n INGRESAR TIPO DE DOCUMENTO:").toUpperCase();
        switch(tp){
            case "FACTURAS": MostrarFactura();break;
            case "PEDIDOS": MostrarPedido();break;
            case "CONTRATOS": MostrarCV();break;
            default: JOptionPane.showMessageDialog(null,"LA RESPUSTA NO CONCUERDA CON LAS OPCIONES","ERROR",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnCambiarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarPedidoActionPerformed
        int ID=Integer.parseInt(JOptionPane.showInputDialog(null,"INGRESE ID DEL PEDIDO"));
        if(CambiarPedido(ID)){
            JOptionPane.showMessageDialog(null,"EL PEDIDO FUE CAMBIADO EXITOSAMENTE","LISTO",JOptionPane.INFORMATION_MESSAGE);
            MostrarPedido();
        }else{
            JOptionPane.showMessageDialog(null,"NOSE SE ENCONTRO PEDIDO","ERROR",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCambiarPedidoActionPerformed

