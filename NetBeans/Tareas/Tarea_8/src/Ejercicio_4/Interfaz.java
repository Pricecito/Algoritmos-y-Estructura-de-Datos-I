    public String Fecha(){
        int dia=Integer.parseInt(JOptionPane.showInputDialog(null,"INGRESAR DIA"));
        String mes=JOptionPane.showInputDialog(null,"INGRESAR NOMBRE DEL MES").toUpperCase();
        int año=Integer.parseInt(JOptionPane.showInputDialog(null,"INGRESAR AÑO"));
        return String.valueOf(dia)+" de "+mes+" del "+String.valueOf(año);
    }
