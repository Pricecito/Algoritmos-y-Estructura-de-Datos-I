    public boolean CambiarPedido(int pos){
        boolean x=false;
        for(int i=0;i<indPed;i++){
            if(pos==i){
                vecPed[i]=new PedidoUrgente(vecPed[i].getFecha(),Fecha(),vecPed[i].getTelefono());
                x=true;
            }
        }
        return x;
    }
