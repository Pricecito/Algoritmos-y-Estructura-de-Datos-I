    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String tex1=txtNom.getText();
        String tex2=txtEdad.getText();
        int persona=cboPersona.getSelectedIndex();
        if(tex1.compareTo("")!=0&&tex1.compareTo("")!=0&&tex1.compareTo("")!=0){
            int gen=cboGener.getSelectedIndex();
            String genero;
            switch(gen){
                case 0: genero="HOMBRE";break;
                default: genero="MUJER";
            }
            String nombre=tex1.toUpperCase();
            int edad=Integer.parseInt(tex2);
            if(edad>18&&edad<60){
                switch(persona){ 
                case 0: if(xEmps==false){
                            sEmps=Double.parseDouble(JOptionPane.showInputDialog(null,"COLOCAR SUELDO DE LOS EMPLEADOS","ENTRADA",JOptionPane.QUESTION_MESSAGE));
                            xEmps=true;
                        }
                        String tEmp=JOptionPane.showInputDialog(null,"1.Ejecutivo\n2.Comercial\nESCRIBIR tipo de empleado:",JOptionPane.QUESTION_MESSAGE).toUpperCase();
                        switch(tEmp){
                            case "EJECUTIVO":Ejecutivo C1=new Ejecutivo(nombre, edad,genero, sEmps,tEmp);
                                            vecEmps[indEmps]=C1;
                                            indEmps++;
                                            break;
                            case "COMERCIAL":Comercial C2=new Comercial(nombre, edad,genero, sEmps,tEmp);
                                            vecEmps[indEmps]=C2;
                                            indEmps++;
                                            break;
                            default: JOptionPane.showMessageDialog(null,"Esta no pertecenece a las alternativas ","ALERTA",JOptionPane.WARNING_MESSAGE);
                        }
                        break;
                case 1:if(xEst==false){
                            sEst=Double.parseDouble(JOptionPane.showInputDialog(null,"COLOCAR SUELDO DE LOS ESTUDIANTES","ENTRADA",JOptionPane.QUESTION_MESSAGE));
                            xEst=true;
                        }
                        Estudiante B2=new Estudiante(nombre, edad,genero, sEst);
                        vecEst[indEst]=B2;
                        indEst++;
                        break;
                default:if(xBec==false){
                            sBec=Double.parseDouble(JOptionPane.showInputDialog(null,"COLOCAR SUELDO DE LOS BECARIOS","ENTRADA",JOptionPane.QUESTION_MESSAGE));
                            xBec=true;
                        }
                        Becario B3=new Becario(nombre, edad,genero, sBec);
                        vecBec[indBec]=B3;
                        indBec++;
                }
            }else{
                JOptionPane.showMessageDialog(null,"DEBE TENER MAS DE 18 AÑOS Y MENOR DE 60","ALERTA",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"HAY CASILLAS VACIAS","ALERTA",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

