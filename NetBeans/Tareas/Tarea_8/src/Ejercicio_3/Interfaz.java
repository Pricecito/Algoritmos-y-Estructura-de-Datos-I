    private void btncSueldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncSueldoActionPerformed
        String per=JOptionPane.showInputDialog(null,"1. EMPLEADO\n2. ESTUDIANTE\n3. BECARIO:",JOptionPane.QUESTION_MESSAGE).toUpperCase();
        double sueldoN=Double.parseDouble(JOptionPane.showInputDialog(null,"INSERTAR NUEVO SUELDO:",JOptionPane.QUESTION_MESSAGE));
        switch(per){
            case "EMPLEADO": 
                    if(sEmps!=0){
                        Empleado.CambiarSalario(sueldoN);
                        sEmps=sueldoN;
                        MostrarEmps();
                    }else{
                        JOptionPane.showMessageDialog(null,"NO SE LE ASIGNO UN SUELDO A LOS EMPLEADOS","ERROR",JOptionPane.WARNING_MESSAGE);
                    }
                    break;
            case "ESTUDIANTE": 
                    if(sEst!=0){
                        Estudiante.CambiarSalario(sueldoN);
                        sEst=sueldoN;
                        MostrarEst();
                    }else{
                        JOptionPane.showMessageDialog(null,"NO SE LE ASIGNO UN SUELDO A LOS ESTUDIANTES","ERROR",JOptionPane.WARNING_MESSAGE);
                    }
                    break;
            case "BECARIO": 
                    if(sBec!=0){
                        Becario.CambiarSalario(sueldoN);
                        sBec=sueldoN;
                        MostrarBecarios();
                    }else{
                        JOptionPane.showMessageDialog(null,"NO SE LE ASIGNO UN SUELDO A LOS BECARIOS","ERROR",JOptionPane.WARNING_MESSAGE);
                    }
                    break;
            default:JOptionPane.showMessageDialog(null,"LA RESPUESTA NO CONCUERDA CON NINGUNA OPCION","ENTRADA",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btncSueldoActionPerformed

